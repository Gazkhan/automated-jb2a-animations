<script>
    import { localize } from "@typhonjs-fvtt/runtime/svelte/helper";
    import SoundSettings from "../soundSettings.svelte";

    import { aaColorMenu } from "../../../animation-functions/databases/jb2a-menu-options.js";

    export let flagData;
    export let isAutoRec;

    /*Data Structure
        section: {
            hunterksmark: {
                menuType: "spell",
                animation: "thunderwave",
                color: String,
                below: Boolean,
                scaleX: Number,
                anchorY: Number,
                scaleY: Number,
                opacity: Number,
                repeat: Number,
                delay: Number,
                removeTemplate: Boolean,
            }
        }
    */

    let root;
    if (isAutoRec) {
        root = flagData;
    } else {
        root = flagData.preset;
    }
    root.thunderwave ? root.thunderwave : (root.thunderwave = {});
    const preset = root.thunderwave;
    preset.menuType = "spell";
    preset.animation = "thunderwave";

    let color =
        preset.color ||
        Object.keys(aaColorMenu.static.spell.thunderwave["mid"])[0];
    $: color = preset.color = color;

    let belowToken = preset.below || false;
    $: belowToken = preset.below = belowToken;
    $: aboveBelow = belowToken ? game.i18n.localize("autoanimations.menus.below") : game.i18n.localize("autoanimations.menus.above");

    let scaleX = preset.scaleX;
    $: scaleX = preset.scaleX = scaleX;

    let scaleY = preset.scaleY;
    $: scaleY = preset.scaleY = scaleY;

    let opacity = preset.opacity || 1;
    $: opacity = opacity > 1 ? 1 : opacity;
    $: preset.opacity = opacity > 1 ? 1 : opacity;

    let repeat = preset.repeat || 1;
    $: repeat = preset.repeat = repeat;

    let delay = preset.delay || 250;
    $: delay = preset.delay = delay;

    function below() {
        belowToken = !belowToken;
    }

    let removeTemplate = preset.removeTemplate || false;
    $: removeTemplate = removeTemplate;
    $: preset.removeTemplate = removeTemplate;
    $: isRemove = removeTemplate ? game.i18n.localize("autoanimations.menus.yes") : game.i18n.localize("autoanimations.menus.no");
    function switchRemove() {
        removeTemplate = !removeTemplate;
    }
</script>

<div class="aaMenu-section">
    <div class="aa-preset-header">
        <div class="flexcol" style="grid-row:1/2; grid-column:3/4">
            <label for="" style="font-size:xx-large; color: rgba(0, 0, 0, 0.6)"
                >{localize("autoanimations.animations.thunderwave")} 5e {localize("autoanimations.menus.preset")}</label
            >
        </div>
    </div>
</div>
<div class="aaMenu-section">
    <div class="aa-3wide">
        <div class="flexcol" style="grid-row: 1 / 2;grid-column: 2 / 3;">
            <label for="">{localize("autoanimations.menus.color")}</label>
            <select bind:value={color}>
                {#each Object.entries(aaColorMenu.static.spell.sneakattack["01"]) as [key, name]}
                    <option value={key}>{name}</option>
                {/each}
            </select>
        </div>
    </div>
    <div class="aa-options-border">
    <h2 style="margin-top:5px; font-size:22px">{localize("autoanimations.menus.options")}</h2>
    <div class="aa-options">
        <!--Set Z-Index-->
        <div class="flexcol" style="grid-row: 1 / 2; grid-column: 1 / 2;">
            <label for="">{localize("autoanimations.menus.level")}</label>
            <button class="oldCheck" on:click={() => below()}
                >{aboveBelow}</button
            >
        </div>
        <!--Remove Template option-->
        <div class="flexcol" style="grid-row: 1 / 2; grid-column: 2 / 3;">
            <label for="">{localize("autoanimations.menus.remove")}</label>
            <button
                class={removeTemplate ? "aa-selected" : "aa-notSelected"}
                on:click={() => switchRemove()}>{isRemove}</button
            >
        </div>
        <div class="flexcol" style="grid-row: 1 / 2; grid-column: 3 / 4;">
            <label for="">{localize("autoanimations.menus.scale")} X</label>
            <input
                type=number
                bind:value={scaleX}
                placeholder=1
                step=0.01
            />
        </div>
        <div class="flexcol" style="grid-row: 1 / 2; grid-column: 4 / 5;">
            <label for="">{localize("autoanimations.menus.scale")} Y</label>
            <input
                type=number
                bind:value={scaleY}
                placeholder=1
                step=0.01
            />
        </div>
        <!--Set Number of times the animation plays-->
        <div class="flexcol" style="grid-row: 2 / 3; grid-column: 1 / 2;">
            <label for="">{localize("autoanimations.menus.repeat")}</label>
            <input type=number bind:value={repeat} placeholder=1 />
        </div>
        <!--Set delay between repeats-->
        <div class="flexcol" style="grid-row: 2 / 3; grid-column: 2 / 3;">
            <label for=""
                >{localize("autoanimations.menus.repeat")}
                {localize("autoanimations.menus.delay")}</label
            >
            <input type=number bind:value={delay} placeholder=250 />
        </div>
        <!--Set Animation Opacity-->
        <div class="flexcol" style="grid-row: 2 / 3; grid-column: 3 / 4;">
            <label for="aaOpacity">{localize("autoanimations.menus.opacity")}</label>
            <div class="form-group">
                <input
                    type=number
                    id="aaOpacity"
                    bind:value={opacity}
                    placeholder=1
                    min=0
                    max=1
                    step=0.01
                    style="font-weight: normal;background:rgb(191 187 182);font-size:medium;height:1.5em;max-width: 3em;"
                />
                <input
                    type="range"
                    min=0
                    max=1
                    step=0.01
                    bind:value={opacity}
                    style="border:none; background:none"
                />
            </div>
        </div>
    </div>
    </div>
    <SoundSettings audioPath="a01" {flagData} />
</div>

